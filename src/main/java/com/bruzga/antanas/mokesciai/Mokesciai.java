    private double dujuKainaKambariui(double kambarioKvadratura) {
        double dujuKainaKambariui =
                (dujuKainaVisamPlotui / visaKvadratura * kambarioKvadratura)
                        + dujosMaistuiBendras / 4
                        + pardavimoMokestis / 4;
        return apvalint(dujuKainaKambariui, 2);
    }

    public static double apvalint(double value, double places) {
        double scale = Math.pow(10, places);
        return Math.round(value * scale) / scale;
    }
