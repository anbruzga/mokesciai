    private void start() {

        validateInput();


        visaKvadratura = parseDouble(Kvadratura1.getText())
                + parseDouble(Kvadratura2.getText())
                + parseDouble(Kvadratura3.getText())
                + parseDouble(Kvadratura4.getText());

        pardavimoMokestis =  parseDouble(paslaugosMokestis.getText());
        double dujosVisos = parseDouble(dujuPrinaudota.getText());
        dujuKaina = parseDouble(dujuKuboKaina.getText());


        dujosMaistuiBendras = 20;
        dujuKainaVisamPlotui = (dujosVisos * dujuKaina)-dujosMaistuiBendras;
        if (dujuKainaVisamPlotui < 1) {
            dujuPrinaudota.setForeground(Color.red);
            dujuKuboKaina.setForeground(Color.red);
            return;
        } else {
            dujuPrinaudota.setForeground(Color.black);
            dujuKuboKaina.setForeground(Color.black);
        }
        dujosMaistuiIndividualus = dujosMaistuiBendras/4;


//9.93, 15.69, 16.44, 22.65
//        double dujos1 = dujuKainaKambariui(10);
//        double dujos2 = dujuKainaKambariui(17);
//        double dujos3 = dujuKainaKambariui(18);
//        double dujos4 = dujuKainaKambariui(24);
        double dujos1 = dujuKainaKambariui(9.93);
        double dujos2 = dujuKainaKambariui(15.69);
        double dujos3 = dujuKainaKambariui(16.44);
        double dujos4 = dujuKainaKambariui(22.65);

        double isVisoDujos = dujos1 + dujos2 + dujos3 + dujos4;


        kvadratura1ResDujos.setText("" + dujos1);
        kvadratura2ResDujos.setText("" + dujos2);
        kvadratura3ResDujos.setText("" + dujos3);
        kvadratura4ResDujos.setText("" + dujos4);
        resDujosTotal.setText("" + apvalint(isVisoDujos,2));


        if (!elektraTotal.getText().equals("") && !vanduoTotal.getText().equals("") ){
            double elektra = parseDouble(elektraTotal.getText());
            double vanduo = parseDouble(vanduoTotal.getText());
            double elektraVanduoIsKeturiu = apvalint((elektra + vanduo) / 4, 2);

            double kvadratu10Total = apvalint(dujos1 + elektraVanduoIsKeturiu,2 );
            double kvadratu15Total = apvalint(dujos2 + elektraVanduoIsKeturiu,2 );
            double kvadratu16Total = apvalint(dujos3 + elektraVanduoIsKeturiu,2 );
            double kvadratu24Total = apvalint(dujos4 + elektraVanduoIsKeturiu,2 );
            double viskas = apvalint(kvadratu10Total + kvadratu15Total + kvadratu16Total + kvadratu24Total,2 );

            kvadratura1Total.setText("" + kvadratu10Total);
            kvadratura2Total.setText("" + kvadratu15Total);
            kvadratura3Total.setText("" + kvadratu16Total);
            kvadratura4Total.setText("" + kvadratu24Total);
            dujosElektraVanduoTotal.setText("" + viskas);

        } else {
            kvadratura1Total.setText("");
            kvadratura2Total.setText("");
            kvadratura3Total.setText("");
            kvadratura4Total.setText("");
            dujosElektraVanduoTotal.setText("");
        }

    }

    public void validateInput() {
        boolean validation1 = true;
        boolean validation2 = true;
        boolean validation3 = true;
        boolean validation4 = true;
        boolean validation5 = true;

        try{
            parseDouble(paslaugosMokestis.getText());
        }
        catch (Exception x){
            validation1 = false;
            paslaugosMokestis.setForeground(Color.red);
        }
        if(validation1){
            paslaugosMokestis.setForeground(Color.black);
        }

        try{
            parseDouble(dujuKuboKaina.getText());
        }
        catch (Exception x){
            validation2 = false;
            dujuKuboKaina.setForeground(Color.red);
        }
        if(validation2){
            dujuKuboKaina.setForeground(Color.black);
        }


        try{
            parseDouble(dujuPrinaudota.getText());
        }
        catch (Exception x){
            validation3 = false;
            dujuPrinaudota.setForeground(Color.red);
        }
        if(validation3){
            dujuPrinaudota.setForeground(Color.black);
        }


        try{
            parseDouble(elektraTotal.getText());
            elektraTotal.setForeground(Color.black);
        }
        catch (Exception x){

            if (!elektraTotal.getText().equals("") && !vanduoTotal.getText().equals("")){
                validation4 = false;
                elektraTotal.setForeground(Color.red);
            } else {
                elektraTotal.setForeground(Color.black);
            }
        }
        try{
            parseDouble(vanduoTotal.getText());
            vanduoTotal.setForeground(Color.black);
        }
        catch (Exception x){

            if (!elektraTotal.getText().equals("") && !vanduoTotal.getText().equals("")){
                validation5 = false;
                vanduoTotal.setForeground(Color.red);
            } else {
                vanduoTotal.setForeground(Color.black);
            }
        }

        if (!validation1 || !validation2 || !validation3  || !validation4 || !validation5 ){
            SkaiciuotiBtn.setForeground(Color.red);
            kvadratura1Total.setText("");
            kvadratura2Total.setText("");
            kvadratura3Total.setText("");
            kvadratura4Total.setText("");
            dujosElektraVanduoTotal.setText("");
            kvadratura1ResDujos.setText("");
            kvadratura2ResDujos.setText("");
            kvadratura3ResDujos.setText("");
            kvadratura4ResDujos.setText("");
            resDujosTotal.setText("");
        } else {
            SkaiciuotiBtn.setForeground(Color.black);
        }


    }

